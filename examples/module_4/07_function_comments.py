

def multiple(num_a, num_b):
    """Multiple num a * num b and return result"""
    return num_a * num_b


def divide(num_a, num_b):
    """Divide number a by number b.

    :param num_a: First number
    :param num_b: second number
    :return: The result of a/b
    """
    return num_a / num_b


